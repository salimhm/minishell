#include "libft.h"

long	ft_atoi(char *str, int *max)
{
	int		i;
	long	r;
	int		a;

	a = 1;
	i = 0;
	r = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\f' || str[i] == '\n'
		|| str[i] == '\v' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			a *= -1;
		i++;
	}
	while (ft_isdigit(str[i]))
		r = (r * 10) + (str[i++] - '0');
	if ((r * a) < -2147483648 || (r * a) > 2147483647)
		*max = 1;
	return (r * a);
}
